// Java program to create a user-defined
// package and function to print
// a message for the users
package userDefinePackage;

// Class
public class gfg {

	public void show()
	{
		System.out.println("Hello geeks!! How are you?");
	}

	public static void main(String args[])
	{
		gfg obj = new gfg();
		obj.show();
	}
}
